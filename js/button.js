$(document).ready(function(){
  $(".btn-primary").mouseover(function(){
    var div = $(".btn-primary");
    div.animate({width: '560px', opacity: '0.8'}, "slow");
    div.animate({width: '140px', opacity: '0.8'}, "slow");
    div.css({"text-align":"right"});
  });
});
